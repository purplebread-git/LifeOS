"""Исключения ядра, не привязанные к конкретному вендору LLM."""


class LLMError(Exception):
    """Базовое исключение для всех ошибок LLM-провайдеров."""


class LLMConnectionError(LLMError):
    """Не удалось установить соединение с API провайдера."""


class LLMTimeoutError(LLMError):
    """Превышено время ожидания ответа от API провайдера."""


class LLMRateLimitError(LLMError):
    """Превышен лимит запросов к API провайдера."""


class ToolExecutionError(Exception):
    """Ожидаемая ошибка выполнения инструмента (неверные аргументы,
    инструмент не найден, отказ внешнего API и т.п.).

    ToolManager ловит ТОЛЬКО это исключение и превращает его в
    ToolResult(is_error=True, ...). Любые другие исключения (AttributeError,
    TypeError, KeyError и т.д. — баги в коде инструмента) должны падать
    дальше и быть видны в тестах и логах, а не маскироваться под обычный
    результат с ошибкой."""